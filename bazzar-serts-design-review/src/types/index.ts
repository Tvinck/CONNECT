/**
 * @fileoverview Глобальные типы данных для проекта Bazzar Certs.
 * Описывают структуру сущностей базы данных (Supabase) и состояния приложения.
 */

/**
 * Представляет собой товар в каталоге магазина.
 */
export interface Product {
  /** Уникальный идентификатор товара (UUID) */
  id: string;
  /** Заголовок (Название) товара */
  title: string;
  /** Краткое описание или подзаголовок */
  subtitle: string;
  /** Категория: 'certs' | 'apps' | 'tools' */
  category: string;
  /** Эмодзи, используемое в качестве fallback-иконки */
  emoji: string;
  /** CSS-строка градиента для обложки (fallback) */
  grad: string;
  /** Абсолютный или относительный URL изображения (обложки) */
  image: string;
  /** Текущая цена в рублях (0, если бесплатно) */
  price: number;
  /** Старая цена для перечеркивания (скидка). Необязательна */
  old_price?: number;
  /** Средний рейтинг товара от 0 до 5 */
  rating: number;
  /** Количество покупок товара */
  sold: number;
  /** Статус бейджика: 'hot' | 'new' | 'sale' | '' */
  badge: string;
  /** Условия или сроки выдачи (например: "Моментально") */
  delivery: string;
  /** Гарантия (например: "1 Год") */
  warranty?: string;
  /** Ссылка на IPA файл (если применимо) */
  ipa_url?: string;
  /** Количество товара в наличии */
  stock: number;
  /** Активен ли товар на витрине */
  active: boolean;
  /** Дата создания в базе данных */
  created_at: string;
}

/**
 * Представляет собой товар, добавленный в корзину пользователя.
 * Расширяет базовую модель `Product` дополнительным полем `qty`.
 */
export interface CartItem extends Product {
  /** Количество единиц данного товара в корзине */
  qty: number;
}

/**
 * Профиль пользователя (клиента), получаемый по UDID.
 */
export interface UserProfile {
  /** Первичный ключ, совпадает с UDID устройства */
  udid: string;
  /** Текущий тарифный план или название последнего купленного товара */
  plan: string;
  /** Статус пользователя: 'bought' | 'pending' | ... */
  status: string;
  /** Дата последней покупки */
  last_purchase: string | null;
  /** Дата создания профиля (регистрации устройства) */
  created_at: string;
}

/**
 * Отзыв о товаре.
 */
export interface Review {
  /** Уникальный идентификатор отзыва (UUID) */
  id: string;
  /** ID товара, к которому относится отзыв */
  product_id: string;
  /** Имя автора отзыва */
  author: string;
  /** Текст отзыва */
  text: string;
  /** Оценка от 1 до 5 */
  rating: number;
  /** Статус модерации: 'published' | 'pending' */
  status: string;
  /** Дата создания отзыва */
  created_at: string;
}
